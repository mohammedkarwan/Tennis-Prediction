from datetime import datetime, date

def is_float(value):
    try:
        float(value)
        return True
    except ValueError:
        return False

def format_date_for_api(date_obj: date) -> str:
    return date_obj.strftime("%Y-%m-%d")

def parse_api_date(date_str: str) -> date:
    return datetime.strptime(date_str, "%Y-%m-%d").date()

def calculate_profit_loss(bet_amount: float, predicted_odd: float, is_win: bool) -> float:
    if is_win:
        return bet_amount * (predicted_odd - 1)
    else:
        return -bet_amount

def get_level(model_probability: float) -> str:
    if model_probability >= 0.8:
        return "High"
    elif model_probability >= 0.7:
        return "Medium"
    else:
        return "Low"